#ifndef API_H
#define API_H

/**
 * Adds two integers.
 *
 * @param a The first integer.
 * @param b The second integer.
 * @return The sum of the two integers.
 */
int add(int a, int b);

/**
 * Subtracts two integers.
 *
 * @param a The first integer.
 * @param b The second integer.
 * @return The result of subtracting b from a.
 */
int subtract(int a, int b);

#endif // API_H
